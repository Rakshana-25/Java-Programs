public class RemainingOperators {
    public static void main (String[] args){
        int x=25;
        int y=19;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y); //Arithmetic operation
        String str="Hello!"; //String concatenation
        str= str + " " + "Rakshana";
        System.out.println(str);
        System.out.println(x>y && x!=y); //Logical operators
        System.out.println(x==y || x!=y);
        System.out.println(x!=y);
        System.out.println(x>y); //Relational operator
        System.out.println(x<y);
        System.out.println(x!=y);
    }
}

